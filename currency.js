const fromCurrency = document.getElementById("fromCurrency");
const toCurrency = document.getElementById("toCurrency");
const convertBtn = document.getElementById("convertBtn");
const resultDiv = document.getElementById("result");
const swapBtn = document.getElementById("swapBtn");
const amountInput = document.getElementById("amount");

for (let currencyCode in country_list) {
    let option1 = document.createElement("option");
    option1.value = currencyCode;
    option1.text = currencyCode;
    fromCurrency.appendChild(option1);

    let option2 = document.createElement("option");
    option2.value = currencyCode;
    option2.text = currencyCode;
    toCurrency.appendChild(option2);
}

fromCurrency.value = "USD";
toCurrency.value = "EUR";

swapBtn.addEventListener("click", () => {
    const temp = fromCurrency.value;
    fromCurrency.value = toCurrency.value;
    toCurrency.value = temp;
});

convertBtn.addEventListener("click", () => {
    const amount = parseFloat(amountInput.value);
    const from = fromCurrency.value;
    const to = toCurrency.value;

    if (isNaN(amount) || amount <= 0) {
        resultDiv.textContent = "Please enter a valid amount.";
        return;
    }

    const apiUrl = `https://api.exchangerate-api.com/v4/latest/${from}`;

    fetch(apiUrl)
        .then((response) => {
            if (!response.ok) throw new Error("Network response was not ok");
            return response.json();
        })
        .then((data) => {
            const rate = data.rates[to];
            if (!rate) {
                resultDiv.textContent = "Currency not supported.";
                return;
            }
            const converted = (amount * rate).toFixed(2);
            resultDiv.textContent = `${amount} ${from} = ${converted} ${to}`;
        })
        .catch((error) => {
            resultDiv.textContent = "Error fetching conversion rate.";
            console.error("Fetch error:", error);
        });
});



